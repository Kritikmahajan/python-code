# In this session  we will be discussing about the list methods.
# Lets start with taking a list.

l=[5, 2, 9, 6, 1, 8, 3, 7, 4]
print(l)

# Starting with list methods.

# 1. list.sort()
# This will arrange the list in ascending order.

l.sort()
print(l)

# 2. list.reverse()
# This will reverse the order of the list.

l.reverse()
print(l)

#3. list.append(a)
# This will arrange the list with 'a' athe the end.

l.append(2)
print(l)

# 4. list.remove(b)
# This will remove 'b' from the list.

l.remove(9)
print(l)

#5. list.insert(c,d)
# This will insert 'd' at index 'c'.

l.insert(3,15)
print(l)

# 6. list.pop(e)
# This will delete the element at 'e' and returns its value.

l.pop(6) 
print(l)

